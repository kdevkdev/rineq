#' @export
confint.hci <-
function(object, parm = NULL, level = 0.95, ...) {
  if (!inherits(object, 'hci')) stop("Object is not of class hci")
  parm <- NULL
  a <- (1 - level) / 2
  a <- c(a, 1 - a)
  fac <- qnorm(a)
  ci <- object$concentration_index + fac * sqrt(object$variance) 
  return(ci)
}

